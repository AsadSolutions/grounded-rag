import { Badge } from "@/components/ui/badge";
import { getEvalResults } from "@/lib/api";

function formatPercent(value: number): string {
  return `${Math.round(value * 100)}%`;
}

function formatDelta(value: number): string {
  const rounded = Math.round(value * 100);
  return `${rounded >= 0 ? "+" : ""}${rounded}%`;
}

export default async function EvalsPage() {
  const results = await getEvalResults();

  return (
    <div className="mx-auto flex w-full max-w-content flex-col gap-8 px-6 py-16">
      <h1 className="font-serif text-[28px] text-text">Evals</h1>
      <p className="max-w-[640px] text-[15px] leading-[1.6] text-muted">
        GroundedRAG grades itself on three numbers. Retrieval hit rate is how
        often the search step actually turns up a chunk containing the
        answer. Groundedness rate is how often the final answer sticks
        strictly to what the cited sources say, with nothing invented.
        Not-found honesty is how often the system correctly admits it
        doesn&apos;t know, instead of guessing, when the documents don&apos;t
        have the answer.
      </p>

      <div className="overflow-hidden rounded-card border border-border bg-surface">
        <table className="w-full border-collapse text-left">
          <thead>
            <tr className="border-b border-border">
              <th className="px-5 py-4 font-serif text-[15px] font-medium text-text">
                Metric
              </th>
              <th className="px-5 py-4 text-right font-serif text-[15px] font-medium text-text">
                Correction OFF
              </th>
              <th className="px-5 py-4 text-right font-serif text-[15px] font-medium text-text">
                Correction ON
              </th>
              <th className="px-5 py-4 text-right font-serif text-[15px] font-medium text-text">
                Delta
              </th>
            </tr>
          </thead>
          <tbody>
            {results.metrics.map((metric, index) => (
              <tr
                key={metric.name}
                className={
                  index < results.metrics.length - 1
                    ? "border-b border-border"
                    : ""
                }
              >
                <td className="px-5 py-4 text-[15px] text-text">
                  {metric.name}
                </td>
                <td className="px-5 py-4 text-right text-[15px] text-muted">
                  {formatPercent(metric.withoutCorrection)}
                </td>
                <td className="px-5 py-4 text-right text-[15px] text-text">
                  {formatPercent(metric.withCorrection)}
                </td>
                <td className="px-5 py-4 text-right">
                  <Badge variant="ok">{formatDelta(metric.delta)}</Badge>
                </td>
              </tr>
            ))}
          </tbody>
        </table>
      </div>

      {results.sample && (
        <p className="text-[13px] text-muted">
          Sample data, real results are generated by the eval harness.
        </p>
      )}
    </div>
  );
}
